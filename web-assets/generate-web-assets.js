#!/usr/bin/env node

import { readdir, readFile, writeFile } from 'node:fs/promises'
import { dirname, relative, resolve, sep } from 'node:path'

const scriptDir = resolve(dirname(new URL(import.meta.url).pathname))
const sourceDir = resolve(scriptDir, 'dist')
const outputPath = resolve(process.argv[2] === '--output' ? process.argv[3] : resolve(scriptDir, 'src', 'FileProxy', 'WebAssets.hs'))

if (process.argv.length !== 2 && (process.argv.length !== 4 || process.argv[2] !== '--output')) {
  throw new Error('usage: generate-web-assets.js [--output PATH]')
}

async function listFiles(directory) {
  const entries = await readdir(directory, { withFileTypes: true })
  const files = await Promise.all(entries.map(async entry => {
    const path = resolve(directory, entry.name)
    if (entry.isDirectory()) return listFiles(path)
    if (entry.isFile()) return [path]
    return []
  }))
  return files.flat()
}

const files = (await listFiles(sourceDir)).sort((left, right) => {
  const leftPath = relative(sourceDir, left).split(sep).join('/')
  const rightPath = relative(sourceDir, right).split(sep).join('/')
  return leftPath.localeCompare(rightPath)
})

const assets = await Promise.all(files.map(async path => ({
  path: relative(sourceDir, path).split(sep).join('/'),
  base64: (await readFile(path)).toString('base64')
})))

const entries = assets.map(({ path, base64 }) => `  (${JSON.stringify(path)}, decode ${JSON.stringify(base64)})`).join(',\n')
const source = `module FileProxy.WebAssets (embeddedFiles) where

import           Data.ByteString (ByteString)
import qualified Data.ByteString.Base64 as Base64
import qualified Data.ByteString.Char8 as BS8

-- Generated by web-assets/generate-web-assets.js. Do not edit manually.
embeddedFiles :: [(FilePath, ByteString)]
embeddedFiles =
  [ ${entries}
  ]

decode :: String -> ByteString
decode encoded =
  case Base64.decode (BS8.pack encoded) of
    Right bytes -> bytes
    Left err -> error $ "invalid generated web asset base64: " ++ err
`

await writeFile(outputPath, source)
